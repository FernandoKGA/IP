class ExerciciosLista1{
    public static void main(String[] args){
        System.out.println(10 + 3 * 4); //Multiplica 3 com 4 e soma com 10.
        System.out.println((10 + 3) * 4); //Soma 10 e 3 e multiplica com 4.
        System.out.println(10 + (3 * 4)); //Multiplica 3 com 4 e soma com 10.
        System.out.println(23 / 4); //Divide 23 com 4 em partes inteiras, resto não aparece.
        System.out.println(4 / 23); //Divide 4 com 23, porém não aparece resultado pois não é possível dividir.
        System.out.println(23 % 4); //Mostra o resto da divisão de 23 com 4 em inteiros.
        System.out.println(4 % 23); //Mostra o resto da divisão de 4 por 23 em inteiros.
        System.out.println("\n");
        System.out.println( 6 * (2 + 3) / (4 + 5));
        System.out.println( (42 / 7) * 5 + 6 * 3 + 4 );
    }
}